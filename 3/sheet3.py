def tree(tu):

    tu.color("saddlebrown")
    tu.begin_fill()
    for i in range(2):
        tu.forward(40)
        tu.left(90)
        tu.forward(10)
        tu.left(90)
    tu.end_fill()
    tu.forward(10)
    tu.left(90)
    tu.forward(5)    
    tu.color("forestgreen")
    tu.begin_fill()
    tu.circle(25)
    tu.end_fill()
    tu.right(90)
